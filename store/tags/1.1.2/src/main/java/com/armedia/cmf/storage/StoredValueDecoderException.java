package com.armedia.cmf.storage;

/**
 * @author diego
 *
 */
public class StoredValueDecoderException extends Exception {
	private static final long serialVersionUID = 1L;

	public StoredValueDecoderException() {
		super();
	}

	public StoredValueDecoderException(String message, Throwable cause) {
		super(message, cause);
	}

	public StoredValueDecoderException(String message) {
		super(message);
	}

	public StoredValueDecoderException(Throwable cause) {
		super(cause);
	}
}