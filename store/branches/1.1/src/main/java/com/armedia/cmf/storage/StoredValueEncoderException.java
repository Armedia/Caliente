package com.armedia.cmf.storage;

/**
 * @author diego
 *
 */
public class StoredValueEncoderException extends Exception {
	private static final long serialVersionUID = 1L;

	public StoredValueEncoderException() {
		super();
	}

	public StoredValueEncoderException(String message, Throwable cause) {
		super(message, cause);
	}

	public StoredValueEncoderException(String message) {
		super(message);
	}

	public StoredValueEncoderException(Throwable cause) {
		super(cause);
	}
}
